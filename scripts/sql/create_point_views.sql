-- 点ジオメトリ VIEW 作成（gaiku ダンプ用）
-- x/y（または hosei_x, hosei_y）と zahyokei_cd を属性のまま保持し、
-- zahyokei_cd に応じた JGD2011 平面直角 SRID で geom を付与する。
--
-- zahyokei_cd → SRID 対応（1 系〜19 系）:
--   1→6669, 2→6670, 3→6671, 4→6672, 5→6673, 6→6674, 7→6675, 8→6676,
--   9→6677, 10→6678, 11→6679, 12→6680, 13→6681, 14→6682, 15→6683,
--   16→6684, 17→6685, 18→6686, 19→6687. それ以外は 6669 にフォールバック。

-- kanri: 座標列は hosei_x, hosei_y
CREATE OR REPLACE VIEW kanri.hojyoten_pt AS
SELECT *,
  ST_SetSRID(
    ST_MakePoint(hosei_x, hosei_y),
    CASE zahyokei_cd
      WHEN 1 THEN 6669 WHEN 2 THEN 6670 WHEN 3 THEN 6671 WHEN 4 THEN 6672
      WHEN 5 THEN 6673 WHEN 6 THEN 6674 WHEN 7 THEN 6675 WHEN 8 THEN 6676
      WHEN 9 THEN 6677 WHEN 10 THEN 6678 WHEN 11 THEN 6679 WHEN 12 THEN 6680
      WHEN 13 THEN 6681 WHEN 14 THEN 6682 WHEN 15 THEN 6683 WHEN 16 THEN 6684
      WHEN 17 THEN 6685 WHEN 18 THEN 6686 WHEN 19 THEN 6687
      ELSE 6669
    END
  )::geometry(Point) AS geom
FROM kanri.hojyoten
WHERE hosei_x IS NOT NULL AND hosei_y IS NOT NULL;

